package com.example.sustainability.api.controller;

public class CategoryCount {

    private String type;
    private int count;

    public CategoryCount(String type, int count) {
        this.type = type;
        this.count = count;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
